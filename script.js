function updateExpoMapImage(lang) {
  if (!expoMapImage) {
    return;
  }

  const dictionary = translations[lang] || translations.th;
  const nextSource = mapImageSources[lang] || mapImageSources.th;

  if (expoMapImage.getAttribute("src") !== nextSource) {
    expoMapImage.src = nextSource;
  }

  expoMapImage.alt = dictionary.mapCaption;
}

function setLanguage(lang) {
  const dictionary = translations[lang] || translations.th;
  currentLanguage = translations[lang] ? lang : "th";

  translatableNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (dictionary[key]) {
      node.textContent = dictionary[key];
    }
  });

  langButtons.forEach((button) => {
    const isActive = button.dataset.lang === lang;
    button.classList.toggle("active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  document.documentElement.lang = lang === "zh" ? "zh-Hans" : lang;
  document.title = dictionary.pageTitle;
  localStorage.setItem("expo-language", lang);
  updateExpoMapImage(currentLanguage);
  updateCountdown();
}

langButtons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

document.querySelectorAll(".nav-panel a").forEach((link) => {
  link.addEventListener("click", () => {
    if (navToggle) {
      navToggle.checked = false;
    }
  });
});

function updateHeaderVisibility() {
  if (!siteHeader) {
    return;
  }

  const currentScrollY = window.scrollY;
  const isScrollingDown = currentScrollY > lastScrollY;
  const isPastHeroTop = currentScrollY > 90;

  siteHeader.classList.toggle("header-scrolled", currentScrollY > 20);
  siteHeader.classList.toggle("header-hidden", isScrollingDown && isPastHeroTop);

  lastScrollY = Math.max(currentScrollY, 0);
}

window.addEventListener("scroll", updateHeaderVisibility, { passive: true });
updateHeaderVisibility();

function formatCountdownPart(value, size = 2) {
  return String(Math.max(0, value)).padStart(size, "0");
}

function updateCountdown() {
  if (!countdownDays || !countdownHours || !countdownMinutes || !countdownSeconds) {
    return;
  }

  const now = new Date();
  const dictionary = translations[currentLanguage] || translations.th;
  let targetDate = eventStartDate;
  let statusText = dictionary.countdownStatusUpcoming;

  if (now >= eventStartDate && now <= eventEndDate) {
    targetDate = eventEndDate;
    statusText = dictionary.countdownStatusOngoing;
  } else if (now > eventEndDate) {
    targetDate = now;
    statusText = dictionary.countdownStatusEnded;
  }

  const totalSeconds = Math.max(0, Math.floor((targetDate - now) / 1000));
  const days = Math.floor(totalSeconds / 86400);
  const hours = Math.floor((totalSeconds % 86400) / 3600);
  const minutes = Math.floor((totalSeconds % 3600) / 60);
  const seconds = totalSeconds % 60;

  countdownDays.textContent = formatCountdownPart(days, 3);
  countdownHours.textContent = formatCountdownPart(hours);
  countdownMinutes.textContent = formatCountdownPart(minutes);
  countdownSeconds.textContent = formatCountdownPart(seconds);

  if (countdownBadge) {
    countdownBadge.textContent = statusText;
  }
}

setInterval(updateCountdown, 1000);

function openLightbox(image) {
  if (!lightbox || !lightboxImage || !lightboxCaption) {
    return;
  }

  const dictionary = translations[currentLanguage] || translations.th;
  const isExpoMap = image.id === "expo-map-image";
  const captionText = isExpoMap ? dictionary.mapCaption : image.alt || "";

  lightboxImage.src = image.currentSrc || image.src;
  lightboxImage.alt = captionText;
  lightboxCaption.textContent = captionText;

  lightbox.classList.add("open");
  lightbox.setAttribute("aria-hidden", "false");
  document.body.style.overflow = "hidden";

  if (lightboxClose) {
    lightboxClose.focus();
  }
}

function closeLightbox() {
  if (!lightbox || !lightboxImage) {
    return;
  }

  lightbox.classList.remove("open");
  lightbox.setAttribute("aria-hidden", "true");
  document.body.style.overflow = "";
  lightboxImage.src = "";
}

document.querySelectorAll(".zoomable-image").forEach((image) => {
  image.setAttribute("tabindex", "0");
  image.setAttribute("role", "button");
  image.addEventListener("click", () => openLightbox(image));
  image.addEventListener("keydown", (event) => {
    if (event.key === "Enter" || event.key === " ") {
      event.preventDefault();
      openLightbox(image);
    }
  });
});

if (lightboxClose) {
  lightboxClose.addEventListener("click", closeLightbox);
}

if (lightbox) {
  lightbox.addEventListener("click", (event) => {
    if (event.target === lightbox) {
      closeLightbox();
    }
  });
}

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape" && lightbox?.classList.contains("open")) {
    closeLightbox();
  }
});

